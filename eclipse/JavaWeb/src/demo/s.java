package demo;

public class s{
	// 属性声明
	private String chang;
	private String kuan;
	private int s;
	// 构造方法的定义
	public s(){}
	// 访问方法
	public String getchang(){ return this.chang; }
	public String getkuan(){ return this.kuan; }
	public int gets(){ return this.s; }
	// 修改方法
	public void setchang(String chang){ this.chang = chang; }
	public void setkuan(String kuan){ this.kuan = kuan; }
	public void sets(int s){ this.s = s; }
}
